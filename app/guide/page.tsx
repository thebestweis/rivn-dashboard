function GuideInfoCard({
  title,
  children,
  tone = "default",
}: {
  title: string;
  children: React.ReactNode;
  tone?: "default" | "emerald" | "violet" | "amber";
}) {
  const toneClassName =
    tone === "emerald"
      ? "border-emerald-400/15 bg-emerald-400/[0.07]"
      : tone === "violet"
      ? "border-violet-400/15 bg-violet-500/[0.07]"
      : tone === "amber"
      ? "border-amber-400/15 bg-amber-400/[0.07]"
      : "border-white/10 bg-white/[0.03]";

  return (
    <div className={`rounded-[24px] border p-5 ${toneClassName}`}>
      <h3 className="text-lg font-semibold text-white">{title}</h3>
      <div className="mt-3 space-y-3 text-sm leading-6 text-white/70">
        {children}
      </div>
    </div>
  );
}

function GuideProse({ children }: { children: React.ReactNode }) {
  return (
    <div className="space-y-4 text-[15px] leading-7 text-white/72">
      {children}
    </div>
  );
}

